        String pname = txtpname.getSelectedItem().toString();
        ppid[j] = txtproductid.getText();
        ppname[j] = pname;
        ppqty[j] = txtquantity.getText();
        cst[j] = csst_temp;
        j++;
        int price = Integer.parseInt(txtprice.getText());
        int qty = Integer.parseInt(txtquantity.getText());
        int tot = qty * price;
        sum = sum + tot;
