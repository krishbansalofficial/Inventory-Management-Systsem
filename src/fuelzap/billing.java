public void billid() {
        conn = MySqlConnect.ConnectDB();
        try {

            String Sql = "SELECT MAX(invoice_id) FROM sales";
            pst = conn.prepareStatement(Sql);
            rs = pst.executeQuery();
            rs.next();
            rs.getString("MAX(invoice_id)");
            if (rs.getString("MAX(invoice_id)") == null) {
                bid = "5001";
            } else {
                long id = Long.parseLong(rs.getString("MAX(Product_Id)"));
                id++;
                bid = id + "";
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

